class EvenOdd{

	public static void main(String args[]){
		
		int Array[] = {0,1,2,3,4,77,5,56,7,64};
		
		for(int i=0;i<10;i++){
			if(i%2==0){
				
				System.out.println(Array[i]+" is even number");
				
				
			}
			else{
				
				System.out.println(Array[i]+" is odd number");
				
			}
			
			
		}
		
		
		
	}


}